#include "libft.h"

void			ft_putnbr(int n)
{
	if (n < 0)
		ft_putchar('-');
	if (n >= 10 || n <= -10)
	{
		ft_putnbr(ft_abs(n / 10));
		ft_putchar(ft_abs(n % 10) + '0');
	}
	else if (n <= 9 || n >= -9)
		ft_putchar(ft_abs(n) + '0');
}
